public class PopupListBean {
    int popupIcon;
    String popupTitle;
    String popupDescribe;

    public int getPopupIcon() {
        return popupIcon;
    }

    public void setPopupIcon(int popupIcon) {
        this.popupIcon = popupIcon;
    }

    public String getPopupTitle() {
        return popupTitle;
    }

    public void setPopupTitle(String popupTitle) {
        this.popupTitle = popupTitle;
    }

    public String getPopupDescribe() {
        return popupDescribe;
    }

    public void setPopupDescribe(String popupDescribe) {
        this.popupDescribe = popupDescribe;
    }
}
